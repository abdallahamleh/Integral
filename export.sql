  CREATE OR REPLACE TRIGGER "HR"."REQUESTS_DONE_UPDATE" 
  after UPDATE of MARKEDDONE ON REQUESTS
  for each row
DECLARE
itemamount NUMBER;
requestamount NUMBER;
temp  NUMBER;
balance NUMBER;
  BEGIN
  SELECT INVITEMAMOUNT into itemamount
  FROM INVENTORY WHERE INVENTORY.INVITEMID = :new.INVITEMID;
    SELECT LASTPURCHASEPRICE into temp FROM INVENTORY WHERE INVENTORY.INVITEMID = :new.INVITEMID;
    SELECT SUPPLIERBALANCE into balance FROM SUPPLIER WHERE SUPPLIER.SUPPLIERID = :new.SUPPLIERID;

  if :new.MARKEDDONE = 1 then
   itemamount := itemamount + :new.REQUESTQUANTITY;
   temp := :new.REQUESTQUANTITY * temp;
   balance := balance + temp;
   UPDATE INVENTORY SET INVITEMAMOUNT = itemamount WHERE INVITEMID = :new.INVITEMID;
   UPDATE SUPPLIER SET SUPPLIERBALANCE = balance WHERE SUPPLIERID = :new.SUPPLIERID;
  end if;
  END;
